CREATE OR ALTER PROCEDURE P_GET_ACTIVE_ITEMS
RETURNS (
    ITEM_ID    D_ID,
    ITEM_NAME  D_NAME,
    ITEM_PRICE DECIMAL(15, 2)
)
AS
BEGIN
  FOR
    SELECT
        I.ITEM_ID,
        I.NAME,
        I.PRICE
    FROM ITEM I
    WHERE I.IS_ACTIVE = 1
    INTO :ITEM_ID, :ITEM_NAME, :ITEM_PRICE
  DO
  BEGIN
    SUSPEND;
  END
END ^

